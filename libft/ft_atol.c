int	ft_atol(const char *nptr, long int *res)
{
	int		i;
	int		sign;
	char	*s;

	*res = 0;
	i = 0;
	s = (char *) nptr;
	sign = 1;
	while (s[i] && (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13)))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		if (*res >= 9223372036854775801
			|| (*res == 922337203685477580 && s[i] > '7' + (sign == -1)))
			return (-1);
		*res = *res * 10 + s[i] - '0';
		i++;
	}
	*res *= sign;
	return (0);
}
